public class CreatingVariable {
	public static void main ( String[] args ) {
		int x, y, age;
		double seconds, e, checking;
		String firstName, last_name, title;

		x = 10;
		y = 400;
		age = 39;

		seconds = 4.71;
		e = 2.315454379579245374792;
		checking = 1.89;
		firstName = "Graham";
		last_name = "Mitchell";
		title = "Mr.";

		System.out.println( "x / seconds = " + x / seconds);
		System.out.println( "y = " + y );
		System.out.println( "age = " + age );
		System.out.println( "seconds = " + seconds );
		System.out.println( "e = " + e );
		System.out.println( "checking = " + checking );
		System.out.println( "My full name is " + title + " " + firstName + last_name);
	}
}